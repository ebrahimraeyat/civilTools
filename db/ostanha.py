    from PyQt4.QtGui import *

    class Window(QWidget):

        def __init__(self):
            super(Window, self).__init__()
            self.box = QComboBox()
            self.shahrBox = QComboBox()
            ostanha = ostans.keys()
            ostanha.sort()
            self.box.addItems(ostanha)
            self.setShahrs()
            self.A = QTextEdit()
            self.setA()
            self.connect(self.box, SIGNAL(
                    "currentIndexChanged(QString)"), self.setShahrs)
            self.connect(self.shahrBox, SIGNAL(
                    "currentIndexChanged(QString)"), self.setA)
            layout = QVBoxLayout()
            layout.addWidget(self.box)
            layout.addWidget(self.shahrBox)
            layout.addWidget(self.A)
            self.setLayout(layout)

        def getShahrs(self, ostan):
            return ostans[ostan].keys()

        def setShahrs(self):
            self.shahrBox.clear()
            ostan = unicode(self.box.currentText())
            shahrs = self.getShahrs(ostan)
            shahrs.sort()
            self.shahrBox.addItems(shahrs)

        def setA(self):
            self.A.clear()
            ostan = unicode(self.box.currentText())
            shahr = unicode(self.shahrBox.currentText())
            A = ostans[ostan][shahr][0]
            self.A.setText(str(A))


    app = QApplication(sys.argv)
    window = Window()
    window.show()
    app.exec_()